void GlobalHandler::clearPlatforms() {
  if (!MPlatformCache.Inst)
    return;
  for (auto &PltSmartPtr : *MPlatformCache.Inst)
    PltSmartPtr->MDevices.clear();
  MPlatformCache.Inst->clear();
}

