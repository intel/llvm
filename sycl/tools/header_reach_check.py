#!/usr/bin/env python
"""
Check that lightweight SYCL public headers do not transitively include
the heavy kernel-launch headers. Exits nonzero on any violation.
"""

import argparse
import os
import subprocess  # nosec B404
import sys

# ──────────────────────────────────────────────────────────────────────
# HEADER LAYERING CONTRACT — read before editing.
#
# These rules guarantee that lightweight public headers do not transitively
# pull in the heavy kernel-launch headers (handler/accessor/buffer/queue/...),
# protecting compile time across the include tree and the KHR split_headers
# surface.
#
# This table is HAND-MAINTAINED, not autogenerated. It encodes what SHOULD be
# true, not a snapshot of what currently is.
#
# If this check fails, the DEFAULT fix is to remove the offending #include or
# break the dependency — NOT to weaken a rule here. Relaxing a rule (removing
# a target or an entry) is a deliberate architectural regression requiring
# justification in the PR description and reviewer sign-off.
# ──────────────────────────────────────────────────────────────────────

