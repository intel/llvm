def get_transitive_includes(clang, header):
    """Run device-only and host compiles, return union of reached sycl/ headers.

    ``clang`` is a list of tokens (the binary plus any leading driver flags,
    e.g. ``--driver-mode=g++`` that lit's %clangxx expands to on Windows).
    """
    reached = set()
    commands = [
        [
            *clang,
            "-fsycl",
            "-fsycl-device-only",
            "-include",
            header,
            "-c",
            "-x",
            "c++",
            os.devnull,
            "-o",
            os.devnull,
            "-MD",
            "-MF",
            "-",
        ],
        [
            *clang,
            "-fsycl",
            "-include",
            header,
            "-c",
            "-x",
            "c++",
            os.devnull,
            "-o",
            os.devnull,
            "-MD",
            "-MF",
            "-",
        ],
    ]
    for cmd in commands:
        result = subprocess.run(cmd, capture_output=True)
        if result.returncode != 0:
            stderr = result.stderr.decode(errors="replace").strip()
            print(
                f"error: compile failed for {header}:\n"
                f"  command: {' '.join(cmd)}\n"
                f"  stderr: {stderr}",
                file=sys.stderr,
            )
            sys.exit(2)
        output = result.stdout.decode(errors="replace")
        if not output.strip():
            stderr = result.stderr.decode(errors="replace").strip()
            print(
                f"error: empty -MD output for {header}:\n"
                f"  command: {' '.join(cmd)}\n"
                f"  stderr: {stderr}",
                file=sys.stderr,
            )
            sys.exit(2)
        # Normalize Windows backslash separators to '/' so the marker match
        # works on both platforms. Make line-continuations ('\' at EOL) and
        # path backslashes both become '/', which split() then tokenizes.
        normalized = output.replace("\\", "/")
        for tok in normalized.split():
            idx = tok.find("/include/sycl/")
            if idx != -1:
                rel = tok[idx + len("/include/sycl/") :]
                reached.add(rel)
    return reached


def main():
    parser = argparse.ArgumentParser(
        description="Check SYCL header transitive-reach layering contract."
    )
    parser.add_argument(
        "--clang",
        required=True,
        nargs=argparse.REMAINDER,
        help="clang++ invocation: binary path plus any trailing driver flags. "
        "Must be the LAST argument. lit's %%clangxx expands to e.g. "
        "'clang.exe --driver-mode=g++' on Windows; REMAINDER captures the "
        "binary and all following tokens verbatim as one command list.",
    )
    args = parser.parse_args()
    if not args.clang:
        parser.error("--clang requires at least the clang++ binary path")

    violations = []
    for entry_header, forbidden_targets in FORBIDDEN_REACH:
        reached = get_transitive_includes(args.clang, entry_header)
        entry_basename = entry_header.split("/")[-1]
        reached.discard(entry_basename)
        for target in forbidden_targets:
            if target in reached:
                violations.append((entry_header, target))

    if violations:
        for entry, target in violations:
            print(
                f"error: header layering violation: {entry} must not "
                f"transitively include {target}, but it does.\n"
                f"       This coupling defeats the split-header compile-time "
                f"guarantee.\n"
                f"       Default fix: remove the offending #include or break "
                f"the dependency.\n"
                f"       Do NOT relax the rule table to silence this without "
                f"PR justification.\n"
            )
        print(f"FAILED: {len(violations)} header layering violation(s) found.")
        sys.exit(1)
    else:
        constrained = sum(1 for _, t in FORBIDDEN_REACH if t)
        print(
            f"PASSED: all {constrained} constrained headers are clean "
            f"({len(FORBIDDEN_REACH)} total entries, "
            f"{len(HEAVY)} forbidden targets max)."
        )
        sys.exit(0)


if __name__ == "__main__":
    main()
